
-- Comparison Operators
-- Comparison operators are used to compare two values in SQL.

-- Example: Select all records where the age is equal to 30
SELECT * FROM Person.Person WHERE Age = 30;

-- Example: Select records where the last name is not 'Smith'
SELECT * FROM Person.Person WHERE LastName <> 'Smith';

-- Example: Select records where the age is less than 25
SELECT * FROM Person.Person WHERE Age < 25;

-- Example: Select records where the age is greater than or equal to 18
SELECT * FROM Person.Person WHERE Age >= 18;
    